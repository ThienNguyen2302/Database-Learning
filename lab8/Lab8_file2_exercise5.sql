CREATE PROCEDURE CAU5_B
AS
BEGIN
	DECLARE @SOHD VARCHAR(10)
	SET @SOHD = DBO.CAU5_A()
	INSERT INTO HOADON VALUES (@SOHD, GETDATE())
END

EXECUTE CAU5_B

--CÂU C:
CREATE PROCEDURE CAU5_C(@MANSX VARCHAR(10), @TENNGK NVARCHAR(30), @DVT NVARCHAR(10), @SOLUONG INT, @DONGIA INT)
AS
BEGIN
	DECLARE @MANGK VARCHAR(10)
	SET @MANGK = (SELECT MAX(MANGK) FROM NUOCGIAIKHAT WHERE MALOAI = @MANSX)
	IF(@MANGK IS NOT NULL)
		BEGIN
			DECLARE @STT INT, @MAMOI VARCHAR(10)
			SET @STT = CONVERT(INT, SUBSTRING(@MANGK, LEN(@MANSX) + 1, 3)) + 1
			IF(@STT < 10)
				SET @MAMOI = @MANSX + '00' + CAST(@STT AS VARCHAR(1))
			ELSE IF(@STT < 100)
				SET @MAMOI = @MANSX + '0' + CAST(@STT AS VARCHAR(2))
			ELSE
				SET @MAMOI = @MANSX + CAST(@STT AS VARCHAR(3))
			INSERT INTO NUOCGIAIKHAT VALUES (@MAMOI, @TENNGK, @DVT, @SOLUONG, @DONGIA, @MANSX)
		END
	ELSE
		INSERT INTO NUOCGIAIKHAT VALUES (@MANSX + '001', @TENNGK, @DVT, @SOLUONG, @DONGIA, @MANSX)
END

EXECUTE CAU5_C 'COCA',N'COCA COLA CHANH','CHAI','300','8000'

--CÂU D:
CREATE PROCEDURE CAU5_D(@SOHD VARCHAR(10), @MANGK VARCHAR(10), @SOLUONG INT)
AS
BEGIN
	DECLARE @SOLUONGSP INT, @DONGIA INT
	SET @SOLUONGSP = (SELECT SOLUONG FROM NUOCGIAIKHAT WHERE MANGK = @MANGK)
	SET @DONGIA = (SELECT DONGIA FROM NUOCGIAIKHAT WHERE MANGK = @MANGK)
	IF(@SOLUONG <= @SOLUONGSP)
		INSERT INTO CHITIETHOADON VALUES (@SOHD, @MANGK, @SOLUONG, @DONGIA * 1.5)
	ELSE
		PRINT N'KHÔNG ĐỦ SỐ LƯỢNG SẢN PHẨM'

END

EXECUTE CAU5_D '1303213','PEP001',1400

--CÂU E:
CREATE FUNCTION CAU5_E(@SOHD VARCHAR(10))
RETURNS INT
AS
BEGIN
	DECLARE @TONGTIEN INT
	SET @TONGTIEN = (SELECT SUM(DONGIA) 
					 FROM CHITIETHOADON 
					 WHERE SOHD = @SOHD
					 GROUP BY SOHD)
	RETURN @TONGTIEN
END

SELECT DBO.CAU5_E('1303213') AS N'TỔNG TIỀN'

--CÂU F:
CREATE FUNCTION CAU5_F()
RETURNS TABLE
AS
	RETURN SELECT DISTINCT NGK.MANGK, TENNGK, NGAYLAP AS N'NGÀY BÁN'
		   FROM NUOCGIAIKHAT NGK, CHITIETHOADON CTHD, HOADON HD
		   WHERE NGK.MANGK = CTHD.MANGK AND HD.SOHD = CTHD.SOHD
				 AND MONTH(HD.NGAYLAP) = 3 AND YEAR(HD.NGAYLAP) = 2021


SELECT * FROM CAU5_F()