--CÂU A:
CREATE FUNCTION CAU5_A()
RETURNS VARCHAR(10)
AS
BEGIN
	DECLARE @SOHD VARCHAR(10), @NGAY VARCHAR(10)
	SET @SOHD = (SELECT MAX(SOHD) FROM HOADON)
	SET @NGAY = CONVERT(VARCHAR(10), GETDATE(), 103)
	SET @NGAY = SUBSTRING(@NGAY,1,2) + SUBSTRING(@NGAY,4,2) + SUBSTRING(@NGAY, 9,2)
	IF(@NGAY = SUBSTRING(@SOHD,1,6))
		BEGIN
			DECLARE @STT INT
			SET @STT = CONVERT(INT, SUBSTRING(@SOHD, 7, LEN(@SOHD) - 6)) + 1
			RETURN @NGAY + CAST(@STT AS VARCHAR(4))
		END
	RETURN @NGAY + '1'
END

SELECT DBO.CAU5_A()

