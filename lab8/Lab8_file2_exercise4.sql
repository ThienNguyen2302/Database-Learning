	MALOP VARCHAR(10) PRIMARY KEY,
	TENLOP NVARCHAR(30) UNIQUE
)

CREATE TABLE SINHVIEN(
	HOTENSV NVARCHAR(50),
	MASV VARCHAR(10) PRIMARY KEY,
	NGAYSINH DATETIME DEFAULT '1/1/2001',
	MALOP VARCHAR(10)
)

