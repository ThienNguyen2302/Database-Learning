CREATE VIEW CAU2A 
AS
SELECT *, YEAR(GETDATE()) - YEAR(NGAYSINH) AS N'TUỔI'
FROM SINHVIEN
WHERE YEAR(GETDATE()) - YEAR(NGAYSINH) <=20 AND HOCLUC>8.5
-- B
CREATE VIEW CAU2B
AS 
SELECT *
FROM DETAI
WHERE KINHPHI > 1000000
--C
CREATE VIEW CAU2C
AS 
SELECT S.MASV, S.TEN, KETQUA, HOCLUC, YEAR(GETDATE()) - YEAR(NGAYSINH) AS N'TUỔI',D.MADT
FROM SINHVIEN S, SINHVIEN_DETAI D
WHERE YEAR(GETDATE()) - YEAR(NGAYSINH) <=20 AND HOCLUC>8 AND KETQUA>8 AND S.MASV = D.MASV
--D
CREATE VIEW CAU2D
AS 
SELECT CNDETAI
FROM DETAI
WHERE MADT IN
(SELECT MADT FROM SINHVIEN_DETAI D, SINHVIEN S WHERE S.MASV = D.MASV AND QUEQUAN = N'TP HỒ CHÍ MINH')
-- E
CREATE VIEW CAU2E
AS 
SELECT *
FROM SINHVIEN
WHERE YEAR(NGAYSINH) <1980 AND QUEQUAN = N'HẢI PHÒNG'
-- CAU F
CREATE VIEW CAU2F
AS 
SELECT AVG(HOCLUC) AS N'ĐIỂM TRUNG BÌNH SV HÀ NỘI'
FROM SINHVIEN
WHERE QUEQUAN = N'HÀ NỘI'
GROUP BY QUEQUAN
-- CAU G
CREATE VIEW CAU2G
AS
SELECT TEN,D.MADT, COUNT(MASV) AS N'SỐ NGƯỜI ĐĂNG KÝ'
FROM SINHVIEN_DETAI S, DETAI D
WHERE S.MADT = 'DT005' AND S.MADT = D.MADT
GROUP BY D.MADT,TEN
-- CAU H
CREATE VIEW CAU2H
AS
SELECT QUEQUAN, COUNT(MASV) AS N'SỐ SINH VIÊN'
FROM SINHVIEN
GROUP BY QUEQUAN

