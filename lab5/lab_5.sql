CREATE DATABASE QUANLYTHUCTAP
USE QUANLYTHUCTAP
CREATE TABLE SINHVIEN(
	MASV CHAR(5) NOT NULL,
	TEN NVARCHAR(50),
	QUEQUAN NVARCHAR(50),
	NGAYSINH DATE,
	HOCLUC REAL
	PRIMARY KEY(MASV)
)
CREATE TABLE DETAI(
	MADT CHAR(5) NOT NULL,
	TEN NVARCHAR(50),
	CNDETAI NVARCHAR(50),
	KINHPHI INT
	PRIMARY KEY(MADT)
)
CREATE TABLE SINHVIEN_DETAI(
	MASV CHAR(5),
	MADT CHAR(5),
	NOITHUCTAP NVARCHAR(50),
	QUANGDUONG INT,
	KETQUA REAL
	PRIMARY KEY (MASV,MADT)
)
